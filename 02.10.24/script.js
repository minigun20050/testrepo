window.addEventListener('DOMContentLoaded', () => {
    let dropImage = document.querySelector('#dropImage1');
    let dragImage = document.querySelector('#dragImage');
    dragImage.addEventListener('dragstart', (e) => {
        e.dataTransfer.setData("text", e.target.id);
    });
    dropImage.addEventListener('dragover', (e) => {
        e.preventDefault();
    });
    dropImage.addEventListener('drop', (e) => {
        e.preventDefault();
        let data = e.dataTransfer.getData("text");
        e.target.appendChild(document.querySelector('#' + data));
    });
});
