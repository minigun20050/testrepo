CREATE DATABASE products;

CREATE TABLE products (
    id INT AUTO_INCREMENT PRIMARY KEY NOT NULL,
    prod_name VARCHAR(50) NOT NULL,
    price DECIMAL(5, 2),
    quantity INT
);
