    id INT AUTO_INCREMENT PRIMARY KEY NOT NULL,
    first_name VARCHAR(50),
    last_name VARCHAR(50),
    email VARCHAR(50),
    country VARCHAR(50),
    city VARCHAR(50),
    street_name VARCHAR(50)
);
CREATE TABLE users_products (
    id_user INT NOT NULL,
    id_product INT NOT NULL,
    INDEX(`id_user`),
    INDEX(`id_product`)
